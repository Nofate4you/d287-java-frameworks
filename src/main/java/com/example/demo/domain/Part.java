package com.example.demo.domain;



import javax.persistence.*;
import javax.validation.constraints.Min;
import java.io.Serializable;
import java.util.HashSet;
import java.util.Set;

/**
 *
 *
 *
 *
 */
@Entity

@Inheritance(strategy = InheritanceType.SINGLE_TABLE)
@DiscriminatorColumn(name="part_type",discriminatorType = DiscriminatorType.INTEGER)
@Table(name="Parts")
public abstract class Part implements Serializable {
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    long id;
    String name;
    @Min(value = 0, message = "Price value must be positive")
    double price;
    @Min(value = 0, message = "Inventory value must be positive")
    int inv;

    // Min and max inventory fields with default column values
    @Column(nullable = false, columnDefinition = "int default 1")
    int minInventory = 1;
    @Column(nullable = false, columnDefinition = "int default 10")
    int maxInventory = 10;

    @ManyToMany
    @JoinTable(name="product_part", joinColumns = @JoinColumn(name="part_id"),
            inverseJoinColumns=@JoinColumn(name="product_id"))
    Set<Product> products= new HashSet<>();

    public Part() {
    }

    public Part(String name, double price, int inv) {
        this.name = name;
        this.price = price;
        this.inv = inv;
    }

    public Part(long id, String name, double price, int inv) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.inv = inv;
    }

    // Adding getters and setters for min and max inventory fields
    public int getMinInventory() {
        return minInventory;
    }
    public void setMinInventory(int minInventory) {
        this.minInventory = minInventory;
    }
    public int getMaxInventory() {
        return maxInventory;
    }
    public void setMaxInventory(int maxInventory) {
        this.maxInventory = maxInventory;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getInv() {
        return inv;
    }

    public void setInv(int inv) {
        this.inv = inv;
    }

    public Set<Product> getProducts() {
        return products;
    }

    public void setProducts(Set<Product> products) {
        this.products = products;
    }

    public String toString(){
        return this.name;
    }
    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Part part = (Part) o;

        return id == part.id;
    }

    @Override
    public int hashCode() {
        return (int) (id ^ (id >>> 32));
    }

    // adding the validator to enforce inventory between min and max
    public String validateInventory() {
        if (this.inv < this.minInventory) {
            return "Inventory is below the minimum allowed parts."; // logic for min validation
        } else if (this.inv > this.maxInventory) {
            return "Inventory is above the maximum allowed parts."; // logic for max validation
        }
        return null;
    }
}


